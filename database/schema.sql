CREATE INDEX idx_positions_symbol ON positions(symbol);
CREATE INDEX idx_trades_portfolio_id ON trades(portfolio_id);
CREATE INDEX idx_trades_symbol ON trades(symbol);
CREATE INDEX idx_trades_executed_at ON trades(executed_at);
CREATE INDEX idx_daily_performance_portfolio_date ON daily_performance(portfolio_id, date);
CREATE INDEX idx_trading_signals_symbol_generated ON trading_signals(symbol, generated_at);
CREATE INDEX idx_recommendations_portfolio_status ON recommendations(portfolio_id, status);
CREATE INDEX idx_market_data_symbol_timestamp ON market_data(symbol, timestamp);
CREATE INDEX idx_news_articles_symbol_published ON news_articles(symbol, published_at);
CREATE INDEX idx_system_logs_timestamp ON system_logs(timestamp);

-- Views for easier querying
CREATE VIEW v_portfolio_summary AS
SELECT
    p.id,
    p.name,
    p.initial_capital,
    p.cash,
    p.total_value,
    (p.total_value - p.initial_capital) as total_pnl,
    ((p.total_value - p.initial_capital) / p.initial_capital * 100) as total_return_percent,
    COUNT(pos.id) as positions_count,
    p.updated_at
FROM portfolios p
LEFT JOIN positions pos ON p.id = pos.portfolio_id
GROUP BY p.id, p.name, p.initial_capital, p.cash, p.total_value, p.updated_at;

CREATE VIEW v_current_positions AS
SELECT
    pos.id,
    pos.portfolio_id,
    pos.symbol,
    pos.shares,
    pos.entry_price,
    pos.current_price,
    (pos.shares * pos.current_price) as current_value,
    ((pos.current_price - pos.entry_price) * pos.shares) as unrealized_pnl,
    (((pos.current_price - pos.entry_price) / pos.entry_price) * 100) as unrealized_pnl_percent,
    pos.stop_loss,
    pos.take_profit,
    pos.entry_date,
    pos.updated_at
FROM positions pos;

CREATE VIEW v_recent_trades AS
SELECT
    t.id,
    t.portfolio_id,
    t.symbol,
    t.side,
    t.shares,
    t.price,
    t.value,
    t.commission,
    t.reason,
    t.executed_at
FROM trades t
ORDER BY t.executed_at DESC;

