-- Trading Simulator Database Schema
-- PostgreSQL for Supabase

-- Users table (for multi-user support in future)
CREATE TABLE users (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    email VARCHAR(255) UNIQUE NOT NULL,
    username VARCHAR(100) UNIQUE NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- Portfolio table - main portfolio information
CREATE TABLE portfolios (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    user_id UUID REFERENCES users(id) ON DELETE CASCADE,
    name VARCHAR(100) NOT NULL DEFAULT 'Main Portfolio',
    initial_capital DECIMAL(15,2) NOT NULL DEFAULT 200.00,
    cash DECIMAL(15,2) NOT NULL DEFAULT 0.00,
    total_value DECIMAL(15,2) NOT NULL DEFAULT 200.00,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- Positions table - current stock positions
CREATE TABLE positions (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    portfolio_id UUID REFERENCES portfolios(id) ON DELETE CASCADE,
    symbol VARCHAR(10) NOT NULL,
    shares DECIMAL(15,6) NOT NULL,
    entry_price DECIMAL(10,4) NOT NULL,
    current_price DECIMAL(10,4) NOT NULL,
    entry_date TIMESTAMP WITH TIME ZONE NOT NULL,
    stop_loss DECIMAL(10,4),
    take_profit DECIMAL(10,4),
    position_type VARCHAR(10) NOT NULL DEFAULT 'long', -- 'long' or 'short'
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    UNIQUE(portfolio_id, symbol) -- One position per symbol per portfolio
);

-- Trades table - all executed trades
CREATE TABLE trades (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    portfolio_id UUID REFERENCES portfolios(id) ON DELETE CASCADE,
    symbol VARCHAR(10) NOT NULL,
    side VARCHAR(4) NOT NULL, -- 'buy' or 'sell'
    shares DECIMAL(15,6) NOT NULL,
    price DECIMAL(10,4) NOT NULL,
    value DECIMAL(15,2) NOT NULL, -- shares * price
    commission DECIMAL(8,2) NOT NULL DEFAULT 0.00,
    trade_type VARCHAR(10) NOT NULL DEFAULT 'market', -- 'market', 'limit', 'stop'
    reason TEXT,
    executed_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- Daily portfolio performance tracking
CREATE TABLE daily_performance (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    portfolio_id UUID REFERENCES portfolios(id) ON DELETE CASCADE,
    date DATE NOT NULL,
    portfolio_value DECIMAL(15,2) NOT NULL,
    cash DECIMAL(15,2) NOT NULL,
    daily_pnl DECIMAL(15,2) NOT NULL,
    daily_return_percent DECIMAL(8,4) NOT NULL,
    total_return_percent DECIMAL(8,4) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    UNIQUE(portfolio_id, date)
);

-- Trading signals generated by the system
CREATE TABLE trading_signals (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    symbol VARCHAR(10) NOT NULL,
    signal_type VARCHAR(20) NOT NULL, -- 'strong_buy', 'buy', 'weak_buy', 'hold', 'weak_sell', 'sell', 'strong_sell'
    confidence DECIMAL(3,2) NOT NULL, -- 0.00 to 1.00
    strength DECIMAL(3,2) NOT NULL, -- 0.00 to 1.00
    current_price DECIMAL(10,4),
    target_price DECIMAL(10,4),
    stop_loss DECIMAL(10,4),
    reasoning TEXT,
    technical_score DECIMAL(3,2),
    news_score DECIMAL(3,2),
    volume_score DECIMAL(3,2),
    momentum_score DECIMAL(3,2),
    generated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- Recommendations generated by the system
CREATE TABLE recommendations (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    portfolio_id UUID REFERENCES portfolios(id) ON DELETE CASCADE,
    action VARCHAR(10) NOT NULL, -- 'buy', 'sell', 'hold', 'reduce', 'add'
    symbol VARCHAR(10) NOT NULL,
    shares DECIMAL(15,6) NOT NULL,
    current_price DECIMAL(10,4) NOT NULL,
    target_price DECIMAL(10,4),
    stop_loss DECIMAL(10,4),
    reasoning TEXT NOT NULL,
    confidence DECIMAL(3,2) NOT NULL,
    urgency VARCHAR(10) NOT NULL, -- 'low', 'medium', 'high'
    expected_return DECIMAL(8,4),
    risk_level VARCHAR(10) NOT NULL, -- 'low', 'medium', 'high'
    time_horizon VARCHAR(10) NOT NULL, -- 'short', 'medium', 'long'
    status VARCHAR(20) NOT NULL DEFAULT 'pending', -- 'pending', 'executed', 'expired', 'rejected'
    generated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    executed_at TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- Market data snapshots
CREATE TABLE market_data (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    symbol VARCHAR(10) NOT NULL,
    current_price DECIMAL(10,4) NOT NULL,
    volume BIGINT,
    daily_change DECIMAL(8,4),
    daily_change_percent DECIMAL(8,4),
    rsi DECIMAL(5,2),
    macd DECIMAL(8,4),
    macd_signal DECIMAL(8,4),
    sma_20 DECIMAL(10,4),
    sma_50 DECIMAL(10,4),
    bb_upper DECIMAL(10,4),
    bb_lower DECIMAL(10,4),
    timestamp TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- News articles and sentiment analysis
CREATE TABLE news_articles (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    symbol VARCHAR(10) NOT NULL,
    title TEXT NOT NULL,
    description TEXT,
    url TEXT,
    source VARCHAR(100),
    published_at TIMESTAMP WITH TIME ZONE NOT NULL,
    sentiment VARCHAR(10), -- 'positive', 'negative', 'neutral'
    sentiment_score DECIMAL(3,2), -- -1.00 to 1.00
    confidence DECIMAL(3,2), -- 0.00 to 1.00
    impact DECIMAL(3,2), -- 0.00 to 1.00
    processed BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- System logs for tracking operations
CREATE TABLE system_logs (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    level VARCHAR(10) NOT NULL, -- 'INFO', 'WARNING', 'ERROR', 'DEBUG'
    module VARCHAR(50) NOT NULL,
    message TEXT NOT NULL,
    metadata JSONB,
    timestamp TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- Performance metrics cache
CREATE TABLE performance_metrics (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    portfolio_id UUID REFERENCES portfolios(id) ON DELETE CASCADE,
    total_return DECIMAL(8,4) NOT NULL,
    annualized_return DECIMAL(8,4) NOT NULL,
    sharpe_ratio DECIMAL(8,4) NOT NULL,
    max_drawdown DECIMAL(8,4) NOT NULL,
    win_rate DECIMAL(5,2) NOT NULL,
    avg_win DECIMAL(10,4) NOT NULL,
    avg_loss DECIMAL(10,4) NOT NULL,
    profit_factor DECIMAL(8,4) NOT NULL,
    total_trades INTEGER NOT NULL,
    days_active INTEGER NOT NULL,
    risk_level VARCHAR(10) NOT NULL,
    performance_grade VARCHAR(1) NOT NULL,
    volatility DECIMAL(8,4) NOT NULL,
    calculated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

