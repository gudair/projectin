-- Functions for updating timestamps
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ language 'plpgsql';

-- Triggers for auto-updating timestamps
CREATE TRIGGER update_portfolios_updated_at BEFORE UPDATE ON portfolios
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

CREATE TRIGGER update_positions_updated_at BEFORE UPDATE ON positions
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

-- Function to calculate portfolio total value
CREATE OR REPLACE FUNCTION calculate_portfolio_value(p_portfolio_id UUID)
RETURNS DECIMAL(15,2) AS $$
DECLARE
    total_value DECIMAL(15,2);
    cash_amount DECIMAL(15,2);
BEGIN
    -- Get cash amount
    SELECT cash INTO cash_amount FROM portfolios WHERE id = p_portfolio_id;

    -- Calculate total position values
    SELECT COALESCE(SUM(shares * current_price), 0) INTO total_value
    FROM positions
    WHERE portfolio_id = p_portfolio_id;

    RETURN cash_amount + total_value;
END;
$$ LANGUAGE plpgsql;